  [...catalogTemplates].sort(
    (a, b) =>
      (b.order ?? 0) - (a.order ?? 0) ||
      b.name.localeCompare(a.name),
  ).slice(0, limit);

const createLatestDesignCard = (template) => {
  const dictionary = translations[activeLanguage];
  const article = createElement("article", "product-card");
  const accent = /^#[0-9a-f]{6}$/i.test(template.accent ?? "")
    ? template.accent
    : "#E13B32";

  const topline = createElement("div", "product-card__topline");
  topline.append(
    createElement("span", "", template.id),
    createElement("span", "", getCategoryLabel(template.category)),
  );

  const preview = createElement("a", "product-preview product-preview--dynamic");
  preview.href = template.previewUrl;
  preview.target = "_blank";
  preview.rel = "noopener noreferrer";
  preview.setAttribute("aria-label", `${dictionary.card.previewOf} ${template.name}`);
  preview.style.setProperty("--product-accent", accent);

  const browser = createElement("div", "preview-browser");
  const browserBar = createElement("div", "preview-browser__bar");
  const dots = createElement("div");
  dots.append(createElement("i"), createElement("i"), createElement("i"));
  browserBar.append(dots, createElement("span", "", template.slug));
  browser.append(browserBar);

  if (template.thumbnail) {
    const image = createElement("img", "product-preview__image");
    image.src = template.thumbnail;
    image.alt = `${dictionary.card.previewOf} ${template.name}`;
    image.loading = "lazy";
    browser.append(image);
  } else {
    const fallback = createElement("div", "latest-screen");
    fallback.style.setProperty("--product-accent", accent);
    fallback.append(
      createElement("small", "", dictionary.catalog.latestBadge),
      createElement("strong", "", template.name),
      createElement("span", "", getCategoryLabel(template.category)),
    );
    browser.append(fallback);
  }

  const previewArrow = createElement("span", "preview-arrow");
  previewArrow.append(createArrowIcon());
  preview.append(browser, previewArrow);

  const info = createElement("div", "product-card__info");
  const heading = createElement("div");
  heading.append(
    createElement(
      "p",
      "product-number",
      String(template.order ?? 1).padStart(2, "0"),
    ),
    createElement("h3", "", template.name),
  );

  const details = createElement("div", "product-card__details");
  details.append(createElement("p", "", getTemplateDescription(template)));

  const tags = createElement("ul", "tag-list");
  tags.setAttribute("aria-label", `${dictionary.catalog.featuresOf} ${template.name}`);
  [template.type, ...(template.styles ?? [])].slice(0, 4).forEach((tag) => {
    tags.append(createElement("li", "", humanize(tag)));
  });

  const actions = createElement("div", "product-actions");
  actions.append(
    createProductButton(dictionary.actions.preview, template.previewUrl, "light", true),
    createProductButton(
      dictionary.actions.choose,
      template.orderUrl || createDefaultOrderUrl(template),
      "line",
    ),
  );

  details.append(tags, actions);
  info.append(heading, details);
  article.append(topline, preview, info);
  return article;
};

const renderLatestDesign = () => {
  if (!latestDesignContainer || !catalogTemplates.length) return;

  const latestTemplates = getLatestTemplates(2);
  if (!latestTemplates.length) return;

  latestDesignContainer.replaceChildren(
    ...latestTemplates.map((template) => createLatestDesignCard(template)),
  );
  latestDesignContainer.setAttribute("aria-busy", "false");
};

const createTemplateCard = (template) => {
  const dictionary = translations[activeLanguage];
  const article = createElement("article", "catalog-card");
  const visual = createElement("a", "catalog-card__visual");
  const accent = /^#[0-9a-f]{6}$/i.test(template.accent ?? "")
    ? template.accent
    : "#E13B32";

  visual.href = template.previewUrl;
  visual.target = "_blank";
  visual.rel = "noopener noreferrer";
  visual.setAttribute(
    "aria-label",
    `${dictionary.card.previewOf} ${template.name}`,
  );
  visual.style.setProperty("--card-accent", accent);

  if (template.thumbnail) {
    const image = createElement("img");
    image.src = template.thumbnail;
    image.alt = `${dictionary.card.previewOf} ${template.name}`;
    image.loading = "lazy";
    visual.append(image);
  } else {
    visual.classList.add("catalog-card__visual--fallback");
    const fallback = createElement("div", "catalog-card__fallback");
    fallback.append(
      createElement("span", "", template.id),
      createElement("strong", "", template.name),
      createElement("i", "", getCategoryLabel(template.category)),
    );
    visual.append(fallback);
  }

  visual.append(
    createElement(
      "span",
      `access-badge access-badge--${template.access}`,
      getAccessLabel(template.access),
    ),
  );

  const body = createElement("div", "catalog-card__body");
  const metadata = createElement("div", "catalog-card__meta");
  metadata.append(
    createElement("span", "", template.id),
    createElement("span", "", getCategoryLabel(template.category)),
  );

  const title = createElement("h3", "", template.name);
  const description = createElement(
    "p",
    "catalog-card__description",
    getTemplateDescription(template),
  );
  const tags = createElement("ul", "catalog-card__tags");

  [template.type, ...(template.styles ?? [])].slice(0, 3).forEach((tag) => {
    tags.append(createElement("li", "", humanize(tag)));
  });

  const footer = createElement("div", "catalog-card__footer");
  const price = createElement("strong", "catalog-card__price", formatPrice(template));
  const actions = createElement("div", "catalog-card__actions");
  actions.append(
    createCatalogButton(dictionary.card.preview, template.previewUrl, "line"),
  );
  actions.append(
    createCatalogButton(
      dictionary.card.choose,
      template.orderUrl || createDefaultOrderUrl(template),
      "solid",
    ),
  );

  footer.append(price, actions);
  body.append(metadata, title, description, tags, footer);
  article.append(visual, body);
  return article;
};

const getFilteredTemplates = () => {
  const normalizedSearch = catalogState.search.trim().toLowerCase();
  const filtered = catalogTemplates.filter((template) => {
    const searchableText = [
      template.name,
      template.category,
      template.type,
      ...(template.styles ?? []),
      template.description?.id,
      template.description?.en,
    ]
      .filter(Boolean)
      .join(" ")
      .toLowerCase();

    return (
      (!normalizedSearch || searchableText.includes(normalizedSearch)) &&
      (catalogState.access === "all" ||
        template.access === catalogState.access) &&
      (catalogState.category === "all" ||
        template.category === catalogState.category)
    );
  });

  return filtered.sort((a, b) => {
    if (catalogState.sort === "name") {
      return a.name.localeCompare(b.name);
    }
    if (catalogState.sort === "newest") {
      return (b.order ?? 0) - (a.order ?? 0);
    }

    return (
      Number(Boolean(b.featured)) - Number(Boolean(a.featured)) ||
      (a.order ?? Number.MAX_SAFE_INTEGER) -
        (b.order ?? Number.MAX_SAFE_INTEGER)
    );
  });
};

const syncCatalogUrl = () => {
  const params = new URLSearchParams();

  if (catalogState.search) params.set("q", catalogState.search);
  if (catalogState.access !== "all") params.set("access", catalogState.access);
  if (catalogState.category !== "all") {
    params.set("category", catalogState.category);
  }
  if (catalogState.sort !== "featured") params.set("sort", catalogState.sort);

  const query = params.toString();
  const nextUrl = `${window.location.pathname}${query ? `?${query}` : ""}${
    window.location.hash
  }`;
  window.history.replaceState({}, "", nextUrl);
};

const renderCategoryOptions = () => {
  if (!categorySelect) return;

  const categories = [...new Set(catalogTemplates.map((item) => item.category))].sort();
  const allOption = createElement(
    "option",
    "",
    translations[activeLanguage].catalogAll.allCategories,
  );
  allOption.value = "all";
  categorySelect.replaceChildren(allOption);

  categories.forEach((category) => {
    const option = createElement("option", "", getCategoryLabel(category));
    option.value = category;
    categorySelect.append(option);
  });

  if (
    catalogTemplates.length > 0 &&
    catalogState.category !== "all" &&
    !categories.includes(catalogState.category)
  ) {
    catalogState.category = "all";
  }

  categorySelect.value = catalogState.category;
};

const renderCatalog = () => {
  if (!templateGrid) return;

  if (!catalogLoaded && !catalogLoadFailed) {
    return;
  }

  if (catalogLoadFailed) {
    templateGrid.textContent = translations[activeLanguage].catalogAll.loadError;
    templateGrid.classList.add("template-grid--error");
    return;
  }

  templateGrid.classList.remove("template-grid--error");
  const filtered = getFilteredTemplates();
  const visibleTemplates = filtered.slice(0, catalogState.visible);
  templateGrid.replaceChildren(
    ...visibleTemplates.map((template) => createTemplateCard(template)),
  );
  templateGrid.setAttribute("aria-busy", "false");

  if (templateCount) {
    templateCount.textContent = String(filtered.length);
  }

  if (catalogEmpty) {
    catalogEmpty.hidden = filtered.length > 0;
  }
  if (loadMoreButton) {
    loadMoreButton.hidden = catalogState.visible >= filtered.length;
  }
};

const resetVisibleItems = () => {
  catalogState.visible = pageSize;
  syncCatalogUrl();
  renderCatalog();
};

const setLanguage = (language) => {
  activeLanguage = translations[language] ? language : "en";
  const dictionary = translations[activeLanguage];

  document.documentElement.lang = activeLanguage;
  document.title = dictionary.meta.title;
  descriptionMeta?.setAttribute("content", dictionary.meta.description);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18nHtml);
    if (typeof value === "string") {
      element.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18nAriaLabel);
    if (typeof value === "string") {
      element.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getValue(dictionary, element.dataset.i18nPlaceholder);
    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });

  document.querySelectorAll("[data-wa-key]").forEach((link) => {
    const message = dictionary.whatsapp[link.dataset.waKey];
    if (message) {
      link.href = `https://wa.me/628131770613?text=${encodeURIComponent(message)}`;
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === activeLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderCategoryOptions();
  renderLatestDesign();
  renderCatalog();
  storeLanguage(activeLanguage);
};

const loadCatalog = async () => {
  try {
    const response = await fetch("./data/templates.json");
    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }

    const data = await response.json();
    catalogTemplates = Array.isArray(data.templates) ? data.templates : [];
    catalogLoaded = true;
    renderCategoryOptions();
    renderLatestDesign();
    renderCatalog();
  } catch (error) {
    catalogLoadFailed = true;
    console.error("Catalog load failed:", error);
    renderCatalog();
  }
};

