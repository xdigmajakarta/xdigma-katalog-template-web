const createElement = (tagName, className, text) => {
  const element = document.createElement(tagName);

  if (className) {
    element.className = className;
  }
  if (text !== undefined) {
    element.textContent = text;
  }

  return element;
};

const createArrowIcon = () => {
  const svg = document.createElementNS("http://www.w3.org/2000/svg", "svg");
  const path = document.createElementNS("http://www.w3.org/2000/svg", "path");
  svg.setAttribute("viewBox", "0 0 24 24");
  svg.setAttribute("aria-hidden", "true");
  path.setAttribute("d", "M7 17 17 7M8 7h9v9");
  svg.append(path);
  return svg;
};

const humanize = (value) =>
  value
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

const getCategoryLabel = (category) =>
  translations[activeLanguage].categories[category] ?? humanize(category);

const getAccessLabel = (access) =>
  access === "free"
    ? translations[activeLanguage].card.free
    : translations[activeLanguage].card.premium;

const formatPrice = (template) => {
  const dictionary = translations[activeLanguage];

  if (template.access === "free") {
    return dictionary.card.free;
  }
  if (template.price === null || template.price === undefined) {
    return dictionary.card.priceOnRequest;
  }

  return new Intl.NumberFormat(activeLanguage === "id" ? "id-ID" : "en-US", {
    style: "currency",
    currency: template.currency || "IDR",
    maximumFractionDigits: 0,
  }).format(template.price);
};

const getTemplateDescription = (template) =>
  template.description?.[activeLanguage] ??
  template.description?.en ??
  template.description?.id ??
  "";

const createCatalogButton = (label, href, variant = "line") => {
  const link = createElement("a", `catalog-button catalog-button--${variant}`);
  link.href = href;
  link.target = "_blank";
  link.rel = "noopener noreferrer";
  link.append(label, createElement("span", "", "↗"));
  return link;
};

const createDefaultOrderUrl = (template) => {
  const message = `${translations[activeLanguage].whatsapp.designInterest} ${template.name}`;
  return `https://wa.me/628131770613?text=${encodeURIComponent(message)}`;
};

const createProductButton = (label, href, variant = "line", withIcon = false) => {
  const link = createElement("a", `button button--${variant}`);
  link.href = href;
  link.target = "_blank";
  link.rel = "noopener noreferrer";
  link.append(createElement("span", "", label));

  if (withIcon) {
    link.append(createArrowIcon());
  }

  return link;
};

